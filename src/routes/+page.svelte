  <h2 style="text-align: center;">Barriers for Infrastructuring with the ENA</h2>
  <h4 style="margin-left:5%;">1. Issues of sovereignty & accreditation</h4>
 <p> <i style="text-align: center;">Excerpt from Interview with EO_1_1a_D</i></p>
  <p style="text-align: center;"> R: So I think one of the issues is people not seeing the value in spending time submitting data.... 
    One thing that we heard time and time again, was how, if somebody in Latin America were to generate data, you know, that's their data. That's their country's kind of like, sovereignty. In that way. It's kind of this bigger question around like, you know,  is the data personal to the country or personal to them or their institution?....
    So generally speaking, people in the higher income countries, Western Europe, US, that kind of thing there's this idea that basically this is a kind of research inequity thing and kinda  post colonial, like grabbing and the maintenance of a country's own data in a separate place, a bit akin to the British Museum, and how it could have, yeah, stole loads of things from around the world and kept him there. So I think that's a very valid thing that people from different countries think. 
