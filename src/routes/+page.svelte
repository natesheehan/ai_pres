  <div style="flex: 1;">
    <h2>The GISAID Comparison</h2>
      <p>GISAID depends on the comprehensiveness of its respondents and participants to be able to provide as wide-ranging a picture of global mutation patters as possible, INSDC is more focused on facilitating linkage between datasets, thereby supporting discovery on a variety of novel aspects of SARS-COV-2 behaviour and interactions with host organisms and environments.</p>
      <p> INSDC members (including the ENA), inadvertently leans towards an object-oriented perspective on Open Science, where the sheer volume of outputs being shared may overshadow diversity and inclusivity</p>
