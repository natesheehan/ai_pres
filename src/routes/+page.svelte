    <h2>The GISAID Issue</h2>
      <p>On the 29th of January 2021 the governing board of EMBL-EBI posted a public letter calling for a greater “openness” in sharing SARS-CoV-2 genome data.</p>
      <p>The letter did not mention GISAID by name, yet it clearly targeted GISAID’s promise to swiftly release protected genomes from their data repository into one that was completely open.</p>
