  <h2 style="text-align: center;">Case Study: Origins</h2>
  <p>- Built originally by European Molecular Biology Lab (EMBL) as the `EMBL data library` at the University of Heidelberg, Germany, in 1982.</p>
  <p>- The first release had 568 entries and was availabile for free by book or magnetic tape.</p>
  <p>- Became a central node in the International Nucleotide Sequence Database Collaboration (INSDC).</p>
  <p>- Continues to grow in volume with the database doubling in size every 12/18 months. </p>
  <p>- Since the 90's has provided digital access to the data via File Transfer Protocols (FTP) servers and CD ROMS.</p>
