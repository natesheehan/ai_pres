Infrastructuring, as Star puts it 
“is in a way a call to study boring things.”
Lists of numbers, technical documentation, hidden mechanisms effecting processes 
Infrastructure in the built environment sense, is often thought of as a system of substrates – roads, train tracks, plumbing, pile ons etc. Something that is part of the background and enables other kinds of work. This becomes a lot more complex when one begins to investigate large socio-technical data infrastructures with historical legacies that are in the making. For a molecular biologist affiliated with a renowned university in the Western hemisphere, such data infrastructures serve as prolific sources for data and research findings. Conversely, for their counterparts situated in the Global South, these infrastructures may be viewed as spaces where contributions to the division of labor are frequently overlooked or underacknowledged. Furthermore, the concept of 'infrastructure' may hold different connotations and challenges depending on an individual's area of focus or expertise. From an analytical standpoint, infrastructure is not recognized as an entity unto itself but rather is discerned only through its relational properties and interactions within a given context.
As Goldstien and Nost suggest, infrastructures are often 
“the invisible things researchers take for granted but are deliberately built and maintained”
Therefore, Ethnographically studying the practices which constitute infrastructures means situating oneself in sites of dissent and diversity, that exist in both, online and AFK reality, and to connect the materiality of a situation with the practices and environment that are made and made from it.  Understanding Infrastructuring as a political and epistemic action requires that we recognise the constraints and possibilities, as well as its constructive and destructive power.  -->

