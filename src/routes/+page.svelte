  <h2 style="text-align: center;">Case Study: Digitalisation</h2>
  <p>Recognising the need for specialised bioinformatics infrastructure, the EMBL council inaugurated the European Bioinformatics Institute, Hixton UK, to inherit the EMBL data library's vast assets in 1994.</p>
 <div style="text-align: center;">
  <i>Excerpt from Interview with M_1_1a_D</i>
  <p>I: So were you part of that transition?</p>
  <p>
    R: Me and M_2_1a_D, who is also working here, are the last two survivors of this time. 
    </p>
    <b>
      [Respondent laughs]
    </b>
    <p>
      I: Did all the magnetic tapes survive?
    </p>
    <p>
R: Well, we had plans to migrate data from the MSI that our operating system runs on from Oracle to Unix. It was a complete disaster and didn't work. And so, RT_7_1a_D just put the computer into the back of his car and drove it over from Heidelberg to here and installed it. I mean, this computer at that time held less data than this phone. But it was, at this time, big data.
  </p>
